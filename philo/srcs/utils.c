#include "philo.h"

size_t	ft_strlen(char *str)
{
	size_t	len;

	len = 0;
	while (str[len])
		len++;
	return (len);
}

long	milliseconds(struct timeval tv)
{
    return (tv.tv_sec * 1000 + tv.tv_usec / 1000);
}
